# arithmetics

print(2 + 5)
print(2 - 5)
print(2 * 5)
print(2 / 5) # integer division in Python 2; floating-point in Python 3

print(2 // 5) # integer division in Python 3

print(10 / 3)
print(10 % 3) # modulo

print(2 ** 3) # 8

# int vs float vs str

print(type(1))
print(type(1.0))
print(type("1"))
print(type(1E3))

print(0.1 + 0.2 - 0.3)
print(0.1 + 0.2)

